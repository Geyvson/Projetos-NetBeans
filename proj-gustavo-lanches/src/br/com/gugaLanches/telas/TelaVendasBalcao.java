    private void inserirPorCodigo() {
        String sql = "select * from tb_produtos where cod_prod = ?";

        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1, txtCod.getText());

            rs = pst.executeQuery();

            if (rs.next()) {
                txtProduto.setText(rs.getString(2));
                txtPreco.setText(rs.getString(3));
                txtCod.setEnabled(false);
                txtQtd.setText("1");
                txtQtd.requestFocus();
                btnAdicionarItem.setEnabled(true);
                btnCancelaItem.setEnabled(true);

            } else {
                JOptionPane.showMessageDialog(null, "Código não encontrado!!!");
                limpartextos();
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void setar() {
        int setar = tblPesqProd.getSelectedRow();

        txtCod.setText(tblPesqProd.getModel().getValueAt(setar, 0).toString());
        txtProduto.setText(tblPesqProd.getModel().getValueAt(setar, 1).toString());
        txtPreco.setText(tblPesqProd.getModel().getValueAt(setar, 2).toString());
        btnAdicionarItem.setEnabled(true);
        btnCancelaItem.setEnabled(true);
        txtQtd.setText("1");
        txtPesqProd.setText(null);
        pesquisaServProd();

    }

