    private void inserirNoCarrinho() {
        int qtd = Integer.parseInt(txtQtd.getText());
        Double prec = Double.valueOf(txtPreco.getText());
        Double valor = qtd * prec;

        if (txtProduto.getText().isEmpty() || txtQtd.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todo os campos!!!");

        } else {
            meus_produtos = (DefaultTableModel) tblCarrinho.getModel();
            meus_produtos.addRow(new Object[]{
                txtCod.getText(),
                txtProduto.getText(),
                txtQtd.getText(),
                txtPreco.getText(),
                valor
            });
            qtdItens += qtd;
            subTotal += valor;
            limpartextos();
            txtSubtotal.setText(subTotal.toString());
            txtQtdItens.setText(String.valueOf(qtdItens));

//            txtQtdItens.setText(qtdItens.toString());
        }
    }

    private void excluirDoCarrinho() {
        int setar = tblCarrinho.getSelectedRow();
        int qtd = Integer.parseInt(tblCarrinho.getModel().getValueAt(setar, 2).toString());
        Double sub = Double.valueOf(tblCarrinho.getModel().getValueAt(setar, 4).toString());

        meus_produtos = (DefaultTableModel) tblCarrinho.getModel();
        meus_produtos.removeRow(tblCarrinho.getSelectedRow());
        btnRemoverItem.setEnabled(false);

        qtdItens -= qtd;
        subTotal -= sub;
        limpartextos();
        txtSubtotal.setText(subTotal.toString());
        txtQtdItens.setText(String.valueOf(qtdItens));

    }

