    public void cancelarPedido() {
        for (int i = tblCarrinho.getRowCount(); i > 0; i--) {
            meus_produtos = (DefaultTableModel) tblCarrinho.getModel();
            meus_produtos.removeRow(0);
        }
        limpartextos();
        qtdItens = 0;
        subTotal = 0.0;
        txtQtdItens.setText(null);
        txtSubtotal.setText(null);
    }

