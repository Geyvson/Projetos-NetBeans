    private void subTotal() {
        Double sub = 0.0;
        Double pix, din, cart;
        Double valorPedido = Double.valueOf(txtValorPedidoteste.getText());

        if (!txtPix.getText().isEmpty()) {
            pix = Double.valueOf(txtPix.getText());
        } else {
            pix = 0.0;
        }

        if (!txtDinheiro.getText().isEmpty()) {
            din = Double.valueOf(txtDinheiro.getText());
        } else {
            din = 0.0;
        }

        if (!txtCartao.getText().isEmpty()) {
            cart = Double.valueOf(txtCartao.getText());
        } else {
            cart = 0.0;
        }

        sub = pix + din + cart;

        txtSubTotal2.setText(sub.toString());
        if (sub >= valorPedido) {
            btnRealizarPagamento.setEnabled(true);
            btnColocarNaConta.setEnabled(false);
        } else {
            btnRealizarPagamento.setEnabled(false);
            btnColocarNaConta.setEnabled(true);
        }

    }

    private void pagar() {
        if (txtSubTotal2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha os valores!!!");
        } else {

            Double subtotal = Double.valueOf(txtSubTotal2.getText());
            Double valorPedido = Double.valueOf(txtValorPedidoteste.getText());

            if (subtotal >= valorPedido) {

                String sql = "insert into tb_vendas(pix, dinheiro, cartao, conta, observacao) values(?, ?, ?, ?, ?)  ";

                try {
                    pst = conn.prepareStatement(sql);
                    pst.setString(1, txtPix.getText());
                    pst.setString(2, txtDinheiro.getText());
                    pst.setString(3, txtCartao.getText());
                    pst.setString(4, "0");
                    pst.setString(5, txtObservacao.getText());

                    int adicionado = pst.executeUpdate();

                    if (adicionado > 0) {

                        JOptionPane.showMessageDialog(null, "Venda realizada!");
                        inserirItensDaVenda();

                    } else {
                        JOptionPane.showMessageDialog(null, "Erro ");
                    }

                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Erro ao Realizar a venda!");
                }

//                JOptionPane.showMessageDialog(null, "Pagamento Realizado \n Valor Pago: " + subtotal + "\n Total do Pedido:  " + valorPedido + "\nTroco: " + (subtotal - valorPedido));
            } else {
                JOptionPane.showMessageDialog(null, "Subtotal MENOR que o Valor do pedido!!!");
            }
        }

    }

    private void troco() {
        Double sub = Double.valueOf(txtSubTotal2.getText());
        Double valorPedido = Double.valueOf(txtValorPedidoteste.getText());
        Double troco;

        if (!txtSubTotal2.getText().isEmpty() && sub > 0) {
            if (sub > valorPedido) {
                troco = sub - valorPedido;
            } else {
                troco = 0.00;
            }
        } else {
            troco = 0.00;
        }

        txtTroco.setText(troco.toString());

    }

    private void pagamentoNaconta() {
        Double valor = Double.valueOf(txtValorPedidoteste.getText());
        Double subtotal = Double.valueOf(txtSubTotal2.getText());
        Double resto = valor - subtotal;

        telaColocarNaConta tela = new telaColocarNaConta();
        tela.txtValorDoDebito.setText(String.valueOf(resto));
        tela.setVisible(true);

    }

