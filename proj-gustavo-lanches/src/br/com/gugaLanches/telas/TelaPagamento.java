    public void inserirItensDaVenda() {
        TelaVendasBalcao tela = new TelaVendasBalcao();
        int idRetorno;
        int estoque;
        for (int i = 0; i > tela.tblCarrinho.getRowCount(); i++) {
            idRetorno = retornaUltimoIdVenda();
            int codProd = Integer.parseInt(tela.tblCarrinho.getValueAt(i, 0).toString());
            int qtdComprada = Integer.parseInt(tela.tblCarrinho.getValueAt(i, 2).toString());
            Double preco = Double.valueOf(tela.tblCarrinho.getValueAt(i, 3).toString());
            Double valor = Double.valueOf(tela.tblCarrinho.getValueAt(i, 4).toString());
            estoque = retornaQtdEstoque(codProd);
            int qtdAtualizada = estoque - qtdComprada;

            salvarItens(idRetorno, codProd, qtdComprada, preco, valor);
            baixaEstoque(qtdAtualizada, codProd);
            tela.pesquisaServProd();

        }
    }

    public int retornaUltimoIdVenda() {

        int ultimoId = 0;

        String sql = "select max(id_venda) id_venda from tb_vendas";

        try {
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();

            while (rs.next()) {
                ultimoId = rs.getInt("id_venda");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return ultimoId;

    }

    public int retornaQtdEstoque(int id) {
        int quant = 0;

        String sql = "select * from tb_produtos where cod_prod=?";

        try {
            pst = conn.prepareStatement(sql);
            pst.setInt(1, id);

            rs = pst.executeQuery();
            if (rs.next()) {
                quant = rs.getInt("qtd_prod");
                return quant;
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao retornar a quantidade do Estoque!");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }

        return quant;
    }

    public void salvarItens(int idVenda, int codProd, int qtd, double preco, double valor) {
        String sql = "insert into tb_intens_da_venda(id_venda, cod_prod, qtd, preco, valor) values(?, ?, ?, ?, ?)";

        try {
            pst = conn.prepareStatement(sql);
            pst.setInt(1, idVenda);
            pst.setInt(2, codProd);
            pst.setInt(3, qtd);
            pst.setDouble(4, preco);
            pst.setDouble(5, valor);

            pst.executeUpdate();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

    private void baixaEstoque(int qtdAtualizada, int codProd) {
        String sql = "updete tb_produtos set qtd_prod=? where=?";

        try {
            pst = conn.prepareStatement(sql);
            pst.setInt(1, qtdAtualizada);
            pst.setInt(2, codProd);

            pst.executeUpdate();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

