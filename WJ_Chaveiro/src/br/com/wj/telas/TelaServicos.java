    private void exclusaoCompleta() {
        int confirma = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja EXCLUIR ESTE SERVIÇO?", "Atenção !!!", JOptionPane.YES_NO_OPTION);

        if (confirma == JOptionPane.YES_OPTION) {

            int id = Integer.parseInt(txtIdServ.getText());
            String teste = verificaDespesa(id);

            excluirItens(id);
            excluirServico(id);

            if (teste.equals("PAGO")) {
                int confirmDespesa = JOptionPane.showConfirmDialog(null, "A comissão já está Paga, Deseja excluir a despesa asimm mesmo?", "Exclusão de Despesa", JOptionPane.YES_OPTION);
                if (confirmDespesa == JOptionPane.YES_OPTION) {
                    excluirDespesa(id);
                } else {
                    JOptionPane.showMessageDialog(null, "A despesa referente a comissão não será removida.");
                }
            } else {
                excluirDespesa(id);
            }

            for (int i = 0; i < tblServCarrinho.getRowCount(); i++) {

                int idRepor = Integer.parseInt(tblServCarrinho.getValueAt(i, 0).toString());
                int qtd = Integer.parseInt(tblServCarrinho.getValueAt(i, 2).toString());
                int estoque = retornaQtdEstoque(idRepor);
                int qtdTotal = estoque + qtd;

                devolverEstoque(qtdTotal, idRepor);
            }

            pesquisaServProd();
            pesquisaServicos();
            limparCampos();
        }
    }

