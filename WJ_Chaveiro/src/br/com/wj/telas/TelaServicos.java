    private String retornaMes() {

        Date hoje = new Date();
        SimpleDateFormat dt = new SimpleDateFormat("MM");
        String a = dt.format(hoje);
        String mes = null;
        switch (a) {
            case "01" ->
                mes = "Jan";
            case "02" ->
                mes = "Fev";
            case "03" ->
                mes = "Mar";
            case "04" ->
                mes = "Abr";
            case "05" ->
                mes = "Mai";
            case "06" ->
                mes = "Jun";
            case "07" ->
                mes = "Jul";
            case "08" ->
                mes = "Ago";
            case "09" ->
                mes = "Set";
            case "10" ->
                mes = "Out";
            case "11" ->
                mes = "Nov";
            case "12" ->
                mes = "Dez";

        }
        return mes;
    }

    private String tiraVirgula(String numComVirg) {
        String semvirgula = new String();
        int t = numComVirg.length();

        for (int i = 0; i < t; i++) {
            if (numComVirg.charAt(i) == ',') {
                semvirgula += '.';
            } else {
                semvirgula += numComVirg.charAt(i);
            }
        }

        return semvirgula;
    }

