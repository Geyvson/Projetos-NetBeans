                String sql = "select * from tb_usuarios where login=? and senha=?";

                try {

                    pst = conn.prepareStatement(sql);
                    pst.setString(1, txtUsuario.getText());
                    String captura = new String(txtSenha.getPassword());
                    pst.setString(2, captura);

                    rs = pst.executeQuery();

                    if (rs.next()) {

                        String perfil = rs.getString(6);

                        if (perfil.equals("Administrador")) {

                            TelaPrincipal tela = new TelaPrincipal();
                            tela.setVisible(true);
                            TelaPrincipal.menDesenvolvedor.setVisible(false);
                            TelaPrincipal.menRel.setEnabled(true);
                            TelaPrincipal.menCadUsu.setEnabled(true);
                            TelaPrincipal.menCadDesp.setEnabled(true);
                            TelaPrincipal.lblPerfil.setText(perfil);
                            TelaPrincipal.lblUsu.setText(rs.getString(2));
                            TelaPrincipal.lblPerfil.setForeground(Color.blue);

                            this.dispose();
                        } else {
                            TelaPrincipal tela = new TelaPrincipal();
                            tela.setVisible(true);
                            TelaPrincipal.menDesenvolvedor.setVisible(false);
                            TelaPrincipal.lblPerfil.setText(perfil);
                            TelaPrincipal.lblUsu.setText(rs.getString(2));
                            this.dispose();
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "Usu√°rio ou Senha Invalidos!!!");
                    }

                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            
