    private void setarCampos() {
        int setar = tblDespesa.getSelectedRow();
        String setado = tblDespesa.getModel().getValueAt(setar, 0).toString();

        String sql = "select * from tb_despesas where id_desp = ?";

        try {

            pst = conn.prepareStatement(sql);
            pst.setString(1, setado);

            rs = pst.executeQuery();

            if (rs.next()) {

                txtDescricao.setText(rs.getString(2));
                txtValor.setText(rs.getString(3));
                cboMesCadastro.setSelectedItem(rs.getString(5));
                

                String tiporbt = rs.getString(4);
                if (tiporbt.equals("Mensal")) {
                    rbtMensal.setSelected(true);
                    tipo = "Mensal";
                } else {
                    rbtFixo.setSelected(true);
                    tipo = "Fixo";
                }
                btnPagar.setEnabled(false);
                btnPesquisar.setEnabled(false);
                btnAdicionar.setEnabled(false);
                btnEditar.setEnabled(true);
                btnExcluir.setEnabled(true);

            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

    private void adicionar() {

        String sql = "insert into tb_despesas(descricao_desp, valor, tipo_desp, mes_ref, situacao)"
                + "values(?, ?, ?, ?, ?)";

        try {

            pst = conn.prepareStatement(sql);
            pst.setString(1, txtDescricao.getText());
            pst.setString(2, txtValor.getText().replace(",", "."));
            pst.setString(3, tipo);
            pst.setString(4, cboMesCadastro.getSelectedItem().toString());
            pst.setString(5, "PENDENTE");

            if (txtDescricao.getText().isEmpty() || txtValor.getText().isEmpty() || cboMesCadastro.getSelectedItem().equals(" ")) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos!!!");

            } else {
                int adicionado = pst.executeUpdate();
                if (adicionado > 0) {
                    JOptionPane.showMessageDialog(null, "Despesa adicionada com sucesso!");
                    limparCampos();
                    limparRelatorio();
                }

            }
            System.out.println(tipo);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

