    private void pesquisaSomaRelatorio() {
        int linhas = tblRelatorio.getRowCount();
        double somaPen = 0;
        double somaPg = 0;

        for (int i = 0; i < linhas; i++) {
            String sit = tblRelatorio.getValueAt(i, 5).toString();
            if (sit.equals("PENDENTE")) {
                somaPen += obj(tblRelatorio.getValueAt(i, 2));

            } else {
                somaPg += obj(tblRelatorio.getValueAt(i, 2));

            }

        }

        txtDespPendentes.setText(String.valueOf(somaPen));
        txtDespPagas.setText(String.valueOf(somaPg));

    }

