    private void setarCampos() {
        int setar = tblTabelaPesquisaProd.getSelectedRow();

        txtCod.setText(tblTabelaPesquisaProd.getModel().getValueAt(setar, 0).toString());
        txtDesc.setText(tblTabelaPesquisaProd.getModel().getValueAt(setar, 1).toString());
        txtQtdAtual.setText(tblTabelaPesquisaProd.getModel().getValueAt(setar, 2).toString());

        btnProdAdicionar.setEnabled(false);
        btnProdEditar.setEnabled(true);
        btnProdExcluir.setEnabled(true);

    }

    private void adicionar() {

        String sql = "insert into tb_produtos(id_prod, descricao_prod, qtd_atual)"
                + "values(?,?,?)";

        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1, txtCod.getText());
            pst.setString(2, txtDesc.getText());
            pst.setString(3, txtQtdAdicionar.getText());

            if (txtCod.getText().isEmpty() || txtDesc.getText().isEmpty() || txtQtdAdicionar.getText().isEmpty()) {

                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatorios!!!");

            } else {

                int ad = pst.executeUpdate();

                if (ad > 0) {
                    JOptionPane.showMessageDialog(null, "Produto inserido com SUCESSO!!!");
                    limparCampos();
                    pesquisarTabelaProd();
                }
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

    private void limparCampos() {
        txtCod.setText(null);
        txtDesc.setText(null);
        txtPesq.setText(null);
        txtQtdAtual.setText(null);
        txtQtdAdicionar.setText(null);

        pesquisarTabelaProdCod();
        btnProdAdicionar.setEnabled(true);
        btnProdEditar.setEnabled(false);
        btnProdExcluir.setEnabled(false);

    }

    private void excluirProd() {

        int confirma = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja EXCLUIR esté produto?", "Atenção !!!", JOptionPane.YES_NO_OPTION);

        if (confirma == JOptionPane.YES_OPTION) {
            String sql = "delete from tb_produtos where id_prod=?";

            try {
                pst = conn.prepareStatement(sql);
                pst.setString(1, txtCod.getText());
                int apagado = pst.executeUpdate();

                if (apagado > 0) {
                    JOptionPane.showMessageDialog(null, "Produto Excluído com sucesso!!");
                    limparCampos();
                    pesquisarTabelaProd();
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }

        }
    }

    private void editarProd() {
        int soma, atual, adic;

        if (!txtQtdAdicionar.getText().isEmpty()) {
            atual = Integer.parseInt(txtQtdAtual.getText());
            adic = Integer.parseInt(txtQtdAdicionar.getText());
            soma = atual + adic;
        } else {
            soma = Integer.parseInt(txtQtdAtual.getText());
        }

        String sql = "update tb_produtos set descricao_prod=?, qtd_atual=? where id_prod=?";

        try {

            pst = conn.prepareStatement(sql);
            pst.setString(1, txtDesc.getText());
            pst.setString(2, String.valueOf(soma));
            pst.setString(3, txtCod.getText());

            if (txtCod.getText().isEmpty() || txtDesc.getText().isEmpty()) {

                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatorios!!!");

            } else {

                int ad = pst.executeUpdate();

                if (ad > 0) {
                    JOptionPane.showMessageDialog(null, "Produto alterado com SUCESSO!!!");
                    limparCampos();
                    pesquisarTabelaProd();
                }
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

