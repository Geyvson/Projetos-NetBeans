    private void pesquisarTabelaProd() {
        String sql = "select id_prod as Código, descricao_prod as descrição, qtd_atual as Quantidade from tb_produtos where descricao_prod like ?";

        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1, "%" + txtPesq.getText() + "%");
            rs = pst.executeQuery();

            tblTabelaPesquisaProd.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

